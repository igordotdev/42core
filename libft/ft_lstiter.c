#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*current;

	if (lst == NULL || f == NULL)
		return ;
	current = lst;
	while (current->next != NULL)
	{
		f(current->content);
		current = current->next;
	}
	f(current->content);
}
